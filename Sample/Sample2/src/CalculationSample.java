public class CalculationSample {
  public static void main(String[] args) {
    System.out.println("四則演算");
    System.out.println(10 + 5);
    System.out.println(10 - 5);
    System.out.println(10 * 5);
    System.out.println(10 / 5);

    System.out.println("割り算の余り");
    System.out.println(10 % 2);
    System.out.println(11 % 2);
  }
}